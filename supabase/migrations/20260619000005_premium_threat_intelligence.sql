CREATE TABLE IF NOT EXISTS brand_guard_threat_intel_jobs (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  threat_id UUID NOT NULL REFERENCES brand_guard_detected_threats(id) ON DELETE CASCADE,
  owner_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  target_url TEXT NOT NULL,
  status TEXT NOT NULL DEFAULT 'queued' CHECK (status IN ('queued', 'leased', 'running', 'completed', 'failed', 'canceled')),
  result JSONB,
  attempt_count INTEGER NOT NULL DEFAULT 0,
  max_attempts INTEGER NOT NULL DEFAULT 3 CHECK (max_attempts BETWEEN 1 AND 5),
  available_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  locked_by TEXT,
  locked_until TIMESTAMPTZ,
  last_error TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  completed_at TIMESTAMPTZ,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (threat_id)
);
CREATE INDEX IF NOT EXISTS idx_bg_intel_claim ON brand_guard_threat_intel_jobs(available_at, created_at) WHERE status = 'queued';

CREATE OR REPLACE FUNCTION enqueue_high_confidence_threat_intel()
RETURNS TRIGGER LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
BEGIN
  IF NEW.threat_type IN ('lookalike', 'phishing_kit') AND NEW.confidence >= 85 AND NEW.status NOT IN ('resolved', 'false_positive') THEN
    INSERT INTO brand_guard_threat_intel_jobs(threat_id, owner_id, target_url)
    VALUES (NEW.id, NEW.owner_id, CASE WHEN NEW.target ~ '^https://' THEN NEW.target ELSE 'https://' || NEW.target END)
    ON CONFLICT (threat_id) DO NOTHING;
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_enqueue_high_confidence_threat_intel ON brand_guard_detected_threats;
CREATE TRIGGER trg_enqueue_high_confidence_threat_intel
AFTER INSERT OR UPDATE OF confidence, status ON brand_guard_detected_threats
FOR EACH ROW EXECUTE FUNCTION enqueue_high_confidence_threat_intel();

