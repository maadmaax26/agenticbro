CREATE OR REPLACE FUNCTION claim_domain_watch_brands(p_worker_id TEXT, p_limit INTEGER DEFAULT 2, p_lease_seconds INTEGER DEFAULT 300)
RETURNS SETOF brand_monitors LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
BEGIN
  RETURN QUERY WITH candidates AS (
    SELECT id FROM brand_monitors
    WHERE is_active = true AND brand_domain IS NOT NULL
      AND COALESCE(domain_watch_next_at, now()) <= now()
      AND (domain_watch_locked_until IS NULL OR domain_watch_locked_until < now())
    ORDER BY domain_watch_next_at NULLS FIRST FOR UPDATE SKIP LOCKED
    LIMIT LEAST(GREATEST(p_limit, 1), 10)
  ) UPDATE brand_monitors b
    SET domain_watch_locked_until = now() + make_interval(secs => p_lease_seconds), updated_at = now()
    FROM candidates c WHERE b.id = c.id RETURNING b.*;
END;
$$;

CREATE OR REPLACE FUNCTION claim_threat_intel_jobs(p_worker_id TEXT, p_limit INTEGER DEFAULT 1, p_lease_seconds INTEGER DEFAULT 300)
RETURNS SETOF brand_guard_threat_intel_jobs LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
BEGIN
  RETURN QUERY WITH candidates AS (
    SELECT id FROM brand_guard_threat_intel_jobs
    WHERE status = 'queued' AND available_at <= now() AND attempt_count < max_attempts
      AND (locked_until IS NULL OR locked_until < now())
    ORDER BY available_at, created_at FOR UPDATE SKIP LOCKED
    LIMIT LEAST(GREATEST(p_limit, 1), 4)
  ) UPDATE brand_guard_threat_intel_jobs j
    SET status = 'leased', locked_by = p_worker_id,
        locked_until = now() + make_interval(secs => p_lease_seconds),
        attempt_count = attempt_count + 1, updated_at = now()
    FROM candidates c WHERE j.id = c.id RETURNING j.*;
END;
$$;

