CREATE TABLE IF NOT EXISTS brand_guard_detected_threats (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  owner_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  brand_monitor_id UUID NOT NULL REFERENCES brand_monitors(id) ON DELETE CASCADE,
  threat_type TEXT NOT NULL CHECK (threat_type IN ('lookalike', 'spoofing', 'phishing_kit')),
  target TEXT NOT NULL,
  resolved_ips TEXT[] NOT NULL DEFAULT '{}',
  mx_records TEXT[] NOT NULL DEFAULT '{}',
  phishing_kit_fingerprint TEXT,
  exfil_endpoints TEXT[] NOT NULL DEFAULT '{}',
  confidence NUMERIC(5,2) NOT NULL DEFAULT 0 CHECK (confidence BETWEEN 0 AND 100),
  severity INTEGER NOT NULL DEFAULT 1 CHECK (severity BETWEEN 1 AND 5),
  status TEXT NOT NULL DEFAULT 'detected' CHECK (status IN ('detected', 'analyzing', 'pending_review', 'takedown_submitted', 'resolved', 'false_positive')),
  evidence JSONB NOT NULL DEFAULT '{}',
  first_seen_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  last_seen_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  last_mutated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (brand_monitor_id, threat_type, target)
);

CREATE INDEX IF NOT EXISTS idx_bg_threats_owner ON brand_guard_detected_threats(owner_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_bg_threats_brand ON brand_guard_detected_threats(brand_monitor_id, last_seen_at DESC);
CREATE INDEX IF NOT EXISTS idx_bg_threats_target ON brand_guard_detected_threats(target);
CREATE INDEX IF NOT EXISTS idx_bg_threats_status ON brand_guard_detected_threats(status, severity DESC);

