CREATE POLICY "Owners manage delivery endpoints" ON brand_guard_delivery_endpoints FOR ALL USING (auth.uid() = owner_id) WITH CHECK (auth.uid() = owner_id);
CREATE POLICY "Owners read delivery jobs" ON brand_guard_delivery_jobs FOR SELECT USING (auth.uid() = owner_id);
CREATE POLICY "Owners read delivery attempts" ON brand_guard_delivery_attempts FOR SELECT USING (job_id IN (SELECT id FROM brand_guard_delivery_jobs WHERE owner_id = auth.uid()));
CREATE POLICY "Owners read dead letters" ON brand_guard_delivery_dead_letters FOR SELECT USING (auth.uid() = owner_id);
CREATE POLICY "Owners read SLA" ON brand_guard_sla_policies FOR SELECT USING (auth.uid() = owner_id);
CREATE POLICY "Owners read enterprise reports" ON brand_guard_enterprise_reports FOR SELECT USING (auth.uid() = owner_id);
CREATE POLICY "Owners read account assignment" ON brand_guard_account_assignments FOR SELECT USING (auth.uid() = owner_id);
CREATE POLICY "Owners read assigned manager" ON brand_guard_account_managers FOR SELECT USING (id IN (SELECT manager_id FROM brand_guard_account_assignments WHERE owner_id = auth.uid()));
CREATE POLICY "Owners manage account cases" ON brand_guard_account_cases FOR ALL USING (auth.uid() = owner_id) WITH CHECK (auth.uid() = owner_id);
CREATE POLICY "Owners read case events" ON brand_guard_account_case_events FOR SELECT USING (case_id IN (SELECT id FROM brand_guard_account_cases WHERE owner_id = auth.uid()));

